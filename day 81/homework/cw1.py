a = int(input("First Number: "))
operator = str(input("Operator: "))
b = int(input("Second Number: "))

if operator == "+":
    print (a + b)
elif operator == "-":
    print (a - b)
elif operator == "*":
    print (a * b)
elif operator == "/":
    print (a / b)
elif operator == "//":
    print (a // b)